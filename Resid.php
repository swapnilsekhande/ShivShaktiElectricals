                                                        </ul>
                                                        <p>&nbsp;</p>
                                                        <p>An upgrade of your residential or commercial electric service may be necessary to increase your existing service capacities. Due to the many high-tech advancements we now enjoy using in our home life, older residences often lack enough power to accommodate escalating electrical demands. This shortage of available power normally requires a comprehensive electric service upgrade requirements review. Any problems with the existing electrical panel usually require a panel upgrade or replacement.</p>
                                                        <p>&nbsp;</p>
                                                        <p>If a breaker becomes overheated, causing burning of a panel buss bar, our electricians will correct this problem. If your original panel installation used aluminum conductors, the conductors between the electrical panel and the meter may have loosened, burning out the primary lugs. If this problem has occurred, our Hall Electrical experts will eliminate this serious issue, making upgrades and replacements as needed. Although large commercial buildings are well equipped with backup electrical systems, our highly experienced electricians at Hall Electrical will perform all necessary repairs, upgrades and replacements to electrical system installations.</p>
